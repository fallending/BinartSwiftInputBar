import Foundation
import IQKeyboardManagerSwift

// 键盘定制

open class KeyboardCustomize: NSObject {
    public static let shared = KeyboardCustomize()
    private override init() {}
    
    var returnKeyHandler: IQKeyboardReturnKeyHandler?
    
    // Call this in UIViewController's loadView
    // Can prevent keyboard's showing push navigationBar's space
//    - (void)loadView
//    {
//        [super loadView];
//        KeyboardCustomize.shared.stand(self)
//    }
    open func stand(context: UIViewController) {
        context.view = UIScrollView.init(frame: UIScreen.main.bounds)
    }
    
    open func enable(context: UIViewController) {
        IQKeyboardManager.shared.enable = true
        IQKeyboardManager.shared.enableAutoToolbar = true
        IQKeyboardManager.shared.shouldResignOnTouchOutside = true
        
        
//        returnKeyHandler = IQKeyboardReturnKeyHandler.init(controller: context)
//        returnKeyHandle
    }
    
    open func disable() {
        IQKeyboardManager.shared.enable = false
        IQKeyboardManager.shared.enableAutoToolbar = false
        IQKeyboardManager.shared.shouldResignOnTouchOutside = false;
    }
}
